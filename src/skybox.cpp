#include "skybox.h"
#include "stb_image.h"
#include <iostream>

Skybox::Skybox(std::vector<std::string> faces) {
    setup();
    texture = loadCubemap(faces);
}

Skybox::~Skybox() {
    glDeleteVertexArrays(1, &vao);
    glDeleteBuffers(1, &vbo);
    glDeleteTextures(1, &texture);
}

